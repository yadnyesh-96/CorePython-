# Real Life usecase Examples Slicing String

print("To get user name")
email="user@gmail.com"
print(email[:])  # print whole string 
print(email[:4],"\n") # prints first 4 characters 

print("To get file extension")
file="filename.txt"
print(file[-4:],"\n")

print("Remive fisrt and Last character from the string")
c="Python"
print(c[:])
print(c[1:5])