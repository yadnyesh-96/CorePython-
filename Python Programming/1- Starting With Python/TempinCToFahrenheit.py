# Formula:
# fah = (cel * 9 / 5) + 32


print("----- Temperature in Celsius and convert it into Fahrenheit -----");

temp=int(input("Enter the Temperature in Celsius: \n"));

fah=(temp*9/5)+32;
print("Temperature(c) ",temp," in Fahrenheit :",fah);

