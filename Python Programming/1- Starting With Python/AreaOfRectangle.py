# Write a Python program to enter length and breadth of a rectangle and find its area.
# Formula:
# area = length * breadth

print("---- Calculate the Area of Rectangle -----");

length=int(input("Enter the Lenght of Rectangle: \n"));
breadth=int(input("Enter the Breadth of Rectangle: \n"));

area=length*breadth;

print("Area of Rectangle is: ",area);